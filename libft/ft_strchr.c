#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;

	i = 0;
	while (*s && *s != c)
	{
		s++;
	}
	if (s[i] == c)
	{
		return ((char *)s);
	}
	return (NULL);
}
